
Vue.filter('formatDate', function(value) {
  if (value) {
    return moment(String(value)).format('DD/MM/YYYY')
  }
})

Vue.filter('formatEuro', function(value) {
  if (value) {
    return Number(value).toLocaleString("es-ES", {minimumFractionDigits: 2}) + " €"
  }
})

